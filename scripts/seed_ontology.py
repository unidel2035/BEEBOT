async def read_existing_symptoms(api: IntegramAPI) -> dict[str, int]:
    """Вернуть {название_симптома: id}."""
    objects = await api.get_all_objects(_TABLE_SYMPTOMS)
    result = {}
    for obj in objects:
        name = obj.get("val", "").strip()
        if name:
            result[name] = obj["id"]
    return result


async def read_products(api: IntegramAPI) -> list[dict]:
    """Вернуть список продуктов [{id, name}]."""
    objects = await api.get_all_objects(_TABLE_PRODUCTS)
    return [{"id": obj["id"], "name": obj.get("val", "").strip()} for obj in objects if obj.get("val")]


def find_product(products: list[dict], keyword: str) -> dict | None:
    """Найти продукт по ключевому слову в имени (регистронезависимо)."""
    kw = keyword.lower()
    for p in products:
        if kw in p["name"].lower():
            return p
    return None


async def main():
    mode = "DRY-RUN" if DRY_RUN else "APPLY"
    print(f"\n{'='*60}")
    print(f"  seed_ontology.py  [{mode}]  mav/mav → bibot")
    print(f"{'='*60}\n")

    api = IntegramAPI()
    try:
        await api.authenticate()
        print("✓ Аутентификация успешна\n")
    except Exception as e:
        print(f"✗ Ошибка аутентификации: {e}")
        return

    # ── 1. Читаем текущие симптомы ─────────────────────────────────────────
    existing = await read_existing_symptoms(api)
    print(f"Уже в базе симптомов: {len(existing)}")
    for name in existing:
        print(f"  • {name}")

    # ── 2. Читаем продукты ─────────────────────────────────────────────────
    products = await read_products(api)
    print(f"\nТоваров в CRM: {len(products)}")
    for p in products:
        print(f"  [{p['id']}] {p['name']}")

    # ── 3. Определяем, что нужно добавить ─────────────────────────────────
    to_add = [(name, kws) for name, kws in NEW_SYMPTOMS if name not in existing]
    print(f"\nНовых симптомов для добавления: {len(to_add)}")
    for name, kws in to_add:
        print(f"  + {name}  ({', '.join(kws[:3])}...)")

    if DRY_RUN:
        print("\n[DRY-RUN] Запустите с --apply для применения изменений.")
        return

    # ── 4. Создаём симптомы ────────────────────────────────────────────────
    print("\nДобавляем симптомы...")
    created_symptoms: dict[str, int] = dict(existing)  # {name: id}

    for name, keywords in to_add:
        try:
            obj_id = await api.create_object(
                _TABLE_SYMPTOMS,
                value=name,
                requisites={_REQ_KEYWORDS: ", ".join(keywords)},
            )
            created_symptoms[name] = obj_id
            print(f"  ✓ [{obj_id}] {name}")
        except Exception as e:
            print(f"  ✗ {name}: {e}")

