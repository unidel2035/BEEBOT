"""Generate reformatted PDF instruction files for BEEBOT.

Mobile-friendly format: A4 portrait, DejaVu Sans (Cyrillic),
warm amber titles, 11pt body text — clean and readable on any screen.

Usage:
    cd /home/hive/BEEBOT
    .venv/bin/python tools/generate_pdfs.py
"""
from pathlib import Path
from xml.sax.saxutils import escape

from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, HRFlowable
from reportlab.lib.styles import ParagraphStyle
from reportlab.lib.pagesizes import A4
from reportlab.lib.units import mm
from reportlab.lib.colors import HexColor
from reportlab.lib.enums import TA_LEFT, TA_CENTER
