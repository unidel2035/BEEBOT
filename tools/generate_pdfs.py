from reportlab.pdfbase.ttfonts import TTFont

# ── Fonts ─────────────────────────────────────────────────────────────────────
FONT_REG  = "/usr/share/fonts/truetype/dejavu/DejaVuSans.ttf"
FONT_BOLD = "/usr/share/fonts/truetype/dejavu/DejaVuSans-Bold.ttf"
pdfmetrics.registerFont(TTFont("DV",  FONT_REG))
pdfmetrics.registerFont(TTFont("DVB", FONT_BOLD))

# ── Paths ─────────────────────────────────────────────────────────────────────
OUTPUT_DIR  = Path(__file__).resolve().parent.parent   # /home/hive/BEEBOT/
TEXTS_DIR   = OUTPUT_DIR / "data" / "texts"

# ── Colors ────────────────────────────────────────────────────────────────────
AMBER      = HexColor("#B8860B")
DARK       = HexColor("#333333")
GRAY       = HexColor("#777777")
BG_WARN    = HexColor("#FFF8E7")

# ── Styles ────────────────────────────────────────────────────────────────────
def _styles():
    return {
        "title": ParagraphStyle(
            "title", fontName="DVB", fontSize=20, leading=26,
            textColor=AMBER, spaceAfter=5*mm, alignment=TA_CENTER,
        ),
        "tagline": ParagraphStyle(
            "tagline", fontName="DV", fontSize=11, leading=15,
            textColor=GRAY, spaceAfter=5*mm, alignment=TA_CENTER,
        ),
        "section": ParagraphStyle(
            "section", fontName="DVB", fontSize=13, leading=18,
            textColor=AMBER, spaceBefore=5*mm, spaceAfter=2*mm,
        ),
        "body": ParagraphStyle(
            "body", fontName="DV", fontSize=11, leading=16,
            textColor=DARK, spaceAfter=3*mm,
        ),
        "bullet": ParagraphStyle(
            "bullet", fontName="DV", fontSize=11, leading=16,
            textColor=DARK, leftIndent=7*mm, firstLineIndent=-5*mm,
            spaceAfter=1.5*mm,
        ),
        "schedule": ParagraphStyle(
            "schedule", fontName="DV", fontSize=10, leading=14,
            textColor=DARK, leftIndent=5*mm, spaceAfter=1*mm,
        ),
        "important": ParagraphStyle(
            "important", fontName="DVB", fontSize=11, leading=16,
            textColor=DARK, spaceBefore=3*mm, spaceAfter=3*mm,
            backColor=BG_WARN, borderColor=AMBER, borderWidth=0.5,
            borderPad=4*mm, borderRadius=2,
        ),
        "disclaimer": ParagraphStyle(
            "disclaimer", fontName="DV", fontSize=9, leading=12,
            textColor=GRAY, alignment=TA_CENTER, spaceBefore=6*mm,
        ),
    }


def make_pdf(filename: str, blocks: list):
    """Build a PDF from a list of (tag, text) blocks.

    Tags: title | tagline | section | body | bullet | schedule | important | hr | disclaimer
    """
    path = OUTPUT_DIR / filename
    doc = SimpleDocTemplate(
        str(path), pagesize=A4,
        leftMargin=18*mm, rightMargin=18*mm,
        topMargin=20*mm, bottomMargin=20*mm,
    )
    S = _styles()
    story = []

    for tag, text in blocks:
        safe = escape(text)
        if tag == "title":
            story.append(Paragraph(safe, S["title"]))
        elif tag == "tagline":
            story.append(Paragraph(safe, S["tagline"]))
        elif tag == "section":
            story.append(Spacer(1, 1*mm))
            story.append(Paragraph(safe, S["section"]))
        elif tag == "body":
            story.append(Paragraph(safe, S["body"]))
        elif tag == "bullet":
            story.append(Paragraph(f"● {safe}", S["bullet"]))
        elif tag == "schedule":
            story.append(Paragraph(safe, S["schedule"]))
        elif tag == "important":
            story.append(Paragraph(safe, S["important"]))
        elif tag == "hr":
            story.append(HRFlowable(
                width="80%", thickness=0.5, color=AMBER,
                spaceBefore=3*mm, spaceAfter=3*mm,
            ))
        elif tag == "disclaimer":
            story.append(HRFlowable(
                width="100%", thickness=0.3, color=GRAY,
                spaceBefore=5*mm, spaceAfter=3*mm,
            ))
            story.append(Paragraph(safe, S["disclaimer"]))

    doc.build(story)
    print(f"  ✓  {filename}")


